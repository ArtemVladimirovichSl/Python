str1 = input("Введите первую строку для проверки:")
str2 = input("Введите вторую строку для поиска в первой строке:")

inkrement = 0
for i in range(len(str1)):
    if str2 in str1[i: i + len(str2)]:
        inkrement += 1
print(inkrement)
